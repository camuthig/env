bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case QWERTY_LINUX:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY_LINUX);
      }
      return false;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
   case VIM_SAVE:
      if (record->event.pressed) {
        tap_code(KC_ESC);
        SEND_STRING(":w");
        tap_code(KC_ENT);
      } else {
         // key released
      }
      return false;
      break;
   case VIM_SAVE_QUIT:
      if (record->event.pressed) {
        tap_code(KC_ESC);
        SEND_STRING(":wq");
        tap_code(KC_ENT);
      } else {
         // key released
      }
      return false;
      break;
   case VIM_FORCE_QUIT:
      if (record->event.pressed) {
        tap_code(KC_ESC);
        SEND_STRING(":q!");
        tap_code(KC_ENT);
      } else {
         // key released
      }
      return false;
      break;
  }
  return true;
}

bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_PGDN);
        } else {
            tap_code(KC_PGUP);
        }
    }
    return true;
}
